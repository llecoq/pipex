#include "libft.h"

int	ft_atoi(const char *str)
{
	int		i;
	int		neg;
	int		retour;

	neg = 1;
	retour = 0;
	i = 0;
	while (str[i] == '\t' || str[i] == '\n' || str[i] == '\v' || str[i] == '\f'
		|| str[i] == '\r' || str[i] == ' ')
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			neg *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		retour *= 10;
		retour += str[i] - 48;
		i++;
	}
	return (retour * neg);
}
